    /**
     *  ASISTENTE.
     * Esta clase crea una nueva entrada, dependiendo del tipo de dato.
     * @param index Recibe el índice de la lista [Estudiante, TrabajoDeGrado, Pregrado, Posgrado]
     * 
     * @data recibe un dato, y el programa reconoce si es un estudiante
     *        o carrera.
     * @index se recibe si no se va a modificar un dato, sino que solamente
     *        lo agrega.
     * 
     */
     public Asistente(int index) {
        initComponents();
        LbTitulo.setText("Agregar Datos");
        // Averiguar que tipo de dato es
        switch (index) {
            case 0:
                // Estudiantes
                // Limpieza
        TfEstudianteTipoDocumento.setSelectedItem(null);
        TfEstudianteEstGraduacion.setSelectedItem(null);
        TfEstudianteEstMatricula.setSelectedItem(null);
        TfEstudianteCarrera.setText("Click acá para seleccionar...");
                // Se esconden los otros combos.
                TpTipo.remove(PanelPosgrado);
                TpTipo.remove(PanelPregrado);
                break;
            case 1:
                // Trabajos de grado
                TpTipo.remove(PanelPosgrado);
                TpTipo.remove(PanelEstudiante);
                TpTipo.remove(PanelPregrado);
                break;
            case 2:
                // Pregrados
                TpTipo.remove(PanelPosgrado);
                TpTipo.remove(PanelEstudiante);
                break;
            case 3:
                // Posgrados
                TpTipo.remove(PanelEstudiante);
                TpTipo.remove(PanelPregrado);
                break;
            default:
                JOptionPane.showMessageDialog(null, "Corre el programa desde la clase principal, ya que no se han cargado aún los datos...");
                this.dispose();
        }
    }
    
    public Asistente(Object data) {
        this.data = data;
        initComponents();
        // Averiguar que tipo de dato es
        index = Operaciones.buscarIndice(data);
        switch (Operaciones.buscarTipo(data)) {
            case 0:
                // Estudiantes
                TpTipo.remove(PanelPosgrado);
                TpTipo.remove(PanelPregrado);
                break;
            case 1:
                // Trabajos de grado
                TpTipo.remove(PanelPosgrado);
                TpTipo.remove(PanelPregrado);
                TpTipo.remove(PanelEstudiante);
                break;
            case 2:
                // Pregrados
                TpTipo.remove(PanelPosgrado);
                TpTipo.remove(PanelEstudiante);
                break;
            case 3:
                // Posgrados
                TpTipo.remove(PanelEstudiante);
                TpTipo.remove(PanelPregrado);
                break;
            default:
                JOptionPane.showMessageDialog(null, "Corre el programa desde la clase principal, ya que no se han cargado aún los datos...");
                this.dispose();
        }
    }

