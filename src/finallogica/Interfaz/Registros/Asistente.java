    private boolean comprobarAdicion(int index){
        boolean check = true;
        switch (index) {
            /* Comprueba que los campos sean válidos, funciona de esta manera:
            
            1. Comprueba que ningun campo este vacio, y que tenga un valor valido.
            2. Comprueba que el tipo de dato sea el correcto, y que no hayan conflictos.
            
            */ 
            case 0:
                check &=(TfEstudianteNombre.getText().trim().length() != 0 );
                check &=(TfEstudianteApellido.getText().trim().length() != 0 );
                check &=(TfEstudianteTipoDocumento.getSelectedIndex() >= 0 );
                check &=(TfEstudianteNumDocumento.getText().trim().length() != 0 );
                check &=(TfEstudianteNumTelefono.getText().trim().length() != 0 );
                check &=(TfEstudianteEstMatricula.getSelectedIndex() >= 0);
                check &=(TfEstudianteEstGraduacion.getSelectedIndex() >= 0 );
                check &=(Operaciones.isInteger(TfEstudianteCarrera.getText()));
                if (check) {
                check &=(Operaciones.isInteger(TfEstudianteNumDocumento.getText()));
                check &=(Operaciones.isInteger(TfEstudianteNumTelefono.getText()));
                if (check) {
                    return true;
                } else {
                    JOptionPane.showMessageDialog(null, "No se ha guardado el estudiante. Revise el número de teléfono o el número de documento sean válidos.", "Error al guardar", JOptionPane.ERROR_MESSAGE);
                
                }
            } else {
                    JOptionPane.showMessageDialog(null, "No se ha guardado el estudiante. Revise que todos los campos esten llenos y sean validos, e intentelo de nuevo.", "Error al guardar", JOptionPane.ERROR_MESSAGE);
                
                }
                break;
            default:
                System.out.println("Asistente.comprobar: Indice de lista no valido : " + index);
        }
        return false;
    }

